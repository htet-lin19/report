package com.model;

import java.util.ArrayList;
import java.util.List;

public class SignUpForm {
	private String emp_cd;
	private String emp_name;
	private String password;
	private int delete_id;
	private int edit_id;
	private int id;
	private String search;
	private String button_event;
	private List<SignUpForm> signUpFormList = new ArrayList<SignUpForm>();
	public String getEmp_cd() {
		return emp_cd;
	}
	public void setEmp_cd(String emp_cd) {
		this.emp_cd = emp_cd;
	}
	public String getEmp_name() {
		return emp_name;
	}
	public void setEmp_name(String emp_name) {
		this.emp_name = emp_name;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	public int getDelete_id() {
		return delete_id;
	}
	public void setDelete_id(int delete_id) {
		this.delete_id = delete_id;
	}
	public int getEdit_id() {
		return edit_id;
	}
	public void setEdit_id(int edit_id) {
		this.edit_id = edit_id;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getSearch() {
		return search;
	}
	public void setSearch(String search) {
		this.search = search;
	}
	public String getButton_event() {
		return button_event;
	}
	public void setButton_event(String button_event) {
		this.button_event = button_event;
	}
	public List<SignUpForm> getSignUpFormList() {
		return signUpFormList;
	}
	public void setSignUpFormList(List<SignUpForm> signUpFormList) {
		this.signUpFormList = signUpFormList;
	}
}
